// Helper functions for demo mode
export class DemoDataProvider {
  static getRandomDemoImages(count: number = 20): ProcessedImage[] {
    const allImages = [...demoImages, ...additionalDemoImages];
    return allImages.slice(0, count);
  }

  static searchDemoImages(
    query: string,
    page: number = 1,
    perPage: number = 20,
  ) {
    const filteredImages = demoImages.filter(
      (img) =>
        img.description?.toLowerCase().includes(query.toLowerCase()) ||
        img.alt_description?.toLowerCase().includes(query.toLowerCase()),
    );

    const start = (page - 1) * perPage;
    const paginatedImages = filteredImages.slice(start, start + perPage);

    return {
      images: paginatedImages,
      total: filteredImages.length,
      totalPages: Math.ceil(filteredImages.length / perPage),
      currentPage: page,
      hasNextPage: page < Math.ceil(filteredImages.length / perPage),
    };
  }

  static getDemoDescription(
    imageId: string,
    style: string,
    language: string = "es",
  ) {
    const descriptions =
      demoDescriptions[imageId as keyof typeof demoDescriptions];
    if (!descriptions) return null;

    const text = descriptions[style as keyof typeof descriptions];
    if (!text) return null;

    return {
      style,
      text,
      language,
      wordCount: text.split(/\s+/).length,
      generatedAt: new Date().toISOString(),
    };
  }

  static getDemoQAPairs(
    imageId: string,
    language: string = "es",
    count: number = 3,
  ) {
    const qaPairs = demoQAPairs[imageId as keyof typeof demoQAPairs];
    if (!qaPairs) return [];

    return qaPairs.slice(0, count);
  }

  static getDemoPhrases(imageId: string, language: string = "es") {
    return (
      demoPhrases[imageId as keyof typeof demoPhrases] || {
        objetos: [],
        acciones: [],
        lugares: [],
        colores: [],
        emociones: [],
        conceptos: [],
      }
    );
  }

  static getDemoImage(id: string): ProcessedImage | null {
    return demoImages.find((img) => img.id === id) || null;
  }

  static generateDemoResponse<T>(data: T, cacheKey: string): T {
    // Simulate API response delay in development
    if (process.env.NODE_ENV === "development") {
      return new Promise((resolve) =>
        setTimeout(() => resolve(data), 500),
      ) as unknown as T;
    }
    return data;
  }
}

